package com.json.myClass;

public class CustomData {
    private int data;
    private int data2;

    public CustomData(int data, int data2) {
        this.data = data;
        this.data2 = data2;
    }
    
    public int getdata() {
        return data;
    }

    public void setdata(int data) {
        this.data = data;
    }

    public int getdata2() {
        return data2;
    }

    public void setdata2(int data2) {
        this.data2 = data2;
    }


 
}